                </p>
                    <p>
                    De este modo  el concepto de ética profesional es aquel que  aplica a todas las situaciones en las cuales el desempeño profesional debe seguir un comportamiento tanto implícito como explícito de reglas morales de diferente tipo, sin generar daños a terceros, ni buscar exclusivamente el propio beneficio de quien las ejerce.
                    </p>
                    <p>
                    Así,  los  principios fundamentales que la profesión del Contador Público o firma  que agrupe a estos profesionistas, deben cumplir con las siguientes:
