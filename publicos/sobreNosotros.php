                      <li>	Objetividad</li>
                      <li>	Diligencia y competencia profesional</li>
                      <li>	Confidencialidad</li>
                      <li>	Comportamiento profesional</li>
                    </ul>
                    </p>
                    <p>
                    En <strong>VHC</strong>, promovemos el comportamiento ético en todos los servicios y actividades inherentes, a esta. Una marca distintiva de la profesión contable es la aceptación de la responsabilidad de servir al interés público, por lo tanto, el deber del Contador Público no es exclusivamente satisfacer las necesidades de un determinado cliente. Por lo cual deberá de cumplir con la Ética Profesional que implica el ejercicio de su vocación.
