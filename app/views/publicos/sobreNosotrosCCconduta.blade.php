        <ol>
          <li>  	Salvaguardar y proteger los equipos eléctricos, software, hardware y cualquier otro tipo de sistema para procesar, almacenar, recibir y transmitir o almacenar información o datos como computadoras, servidores, bases de datos, teléfonos, equipos inalámbricos, equipos móviles, correo electrónico, conexión a Internet, incluyendo aquellos equipos que no son propiedad de la firma.
          </li>
          <li> 	Aplicar correcta, transparente y responsablemente los recursos de VHC, evitando cualquier uso discrecional o desvío en la disposición de los mismos.
          </li>
          <li>  	No asignar remuneraciones fuera de la nómina o de los presupuestos previamente aprobados para los proyectos, salvo casos fortuitos o de fuerza mayor, o lo amerite así la situación, previa autorización por escrito del cliente y del socio responsable del proyecto.
          </li>
          <li>  	Llevar un control de los recursos dispuestos para los proyectos debidamente aprobados.
          </li>
          <li> 	Utilizar las oficinas, el mobiliario y el equipo de transporte asignado, para los fines que les sean propios.
          </li>
          <li> 	Disponer de las relaciones establecidas únicamente para los fines que le sean propios, nunca para obtener favores especiales o beneficios personales.
          </li>
          <li> 	Observar pleno respeto al equilibrio ecológico, promover el uso racional de los recursos naturales y el desarrollo sustentable en el desempeño de sus labores.
          </li>
          <li> 	Usar responsable y productivamente el teléfono, correo electrónico e Internet. Queda prohibido al acceso a sitios de internet a donde se solicite o promueva pornografía, segregación racial, discriminación sexual, religiosa, social o política, etc., así como a videojuegos, chats, redes sociales.
          </li>
          <li> 	Dar un uso adecuado y responsable a los activos constituidos por nuestra firma con la finalidad de mantener actualizados a los miembros de la misma, tales como: Biblioteca, Videoteca, Hemeroteca y el Sistema interno de boletines informativos.
          </li>
          <li> 	Evitar descargar en los equipos de cómputo música, imágenes, video o cualquier otro dato o archivo que ponga en riesgo la seguridad de todo el sistema de información digital y de comunicación interna y externa.
          </li>
        </ol>

        <h3>VI.	Compromiso de los miembros de VHC</h3>
        <ol>
