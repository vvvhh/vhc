        <h2>Nuestro Código de Conducta</h2>

        <h3>I.	Disposiciones generales</h3>
        <ol>
          <li  value="1"> 	Se espera que los integrantes de VHC, conozcan y contribuyan al logro de la Misión y la Visión, mediante la práctica de los Valores y el cumplimiento de lo establecido en este Código de Conducta.
          </li>
          <li  value="2"> 	Este código deberá ser asumido como un auténtico compromiso personal por todos los integrantes de VHC.
          </li>
          <li  value="3"> Los integrantes de VHC, cumplirán el presente código en el desempeño de sus cargos y lo difundirán entre su equipo de colaboradores.
          </li>
          <li  value="4"> Será un instrumento de evaluación del comportamiento profesional.
          </li>
          <li  value="5"> Los colaboradores ejercerán sus deberes con estricta observancia y apego a las políticas internas, a la normatividad profesional y a las disposiciones legales aplicables.
          </li>
        </ol>

        <h3>II.	Compromiso de los integrantes de VHC con su responsabilidad asignada, y con nuestros clientes</h3>
        <ol>
          <li value="1"> Conocer la naturaleza y amplitud de las facultades del cargo que le corresponde desempeñar, para ello VHC informará, capacitará y supervisará a sus miembros permanentemente para cumplirlo con profesionalismo.
          </li>
          <li value="2"> Conocer y dominar los procedimientos aplicables a su cargo.
          </li>
          <li value="3"> Ofrecer la atención al cliente, con igualdad de oportunidades, sin discriminación alguna que pueda dar lugar a trato diferenciado o preferente.
          </li>
          <li value="4"> Actuar con ética y justicia en todo momento, con voluntad permanente de ofrecer el mejor servicio en cada situación. Comprender que la confianza que nuestra firma ha logrado generar en sus clientes, es debido a la calidad de sus servicios, la cual es responsabilidad de todos y cada uno de los que integramos VHC.
          </li>
          <li value="5"> Brindar atención en el cumplimiento de sus funciones de forma: cordial, pronta, oportuna, diligente, expedita y respetuosa.
          </li>
          <li value="6"> Ofrecer únicamente aquellos servicios que estamos en capacidad de cumplir, para los cuales poseemos la experiencia necesaria y conocimiento profesional, mismos que deberán encontrarse soportados documentalmente mediante una propuesta o contrato de servicios.</li>
          <li value="7"> Aceptar únicamente aquellos trabajos para los cuales se tiene la experiencia, capacidad y conocimiento profesional adecuados.
          </li>
          <li value="8"> Evitar hacer afirmaciones exageradas de los servicios que podemos ofrecer, de las calificaciones que tenemos o de la experiencia que hemos logrado.
          </li>
          <li value="9"> Desempeñar el cargo y la responsabilidad conferida con lealtad, con decisión y profesionalismo.
          </li>
          <li value="10"> 	Evitar hacer referencias negativas o comparaciones sin fundamento respecto al trabajo desarrollado por colegas.</li>
          <li value="11"> 	Ejecutar nuestras funciones con la intensidad, cuidado y esmero apropiados, en la forma, tiempo y lugar convenidos.</li>
        </ol>

        <h3>III.	Desinterés e Imparcialidad</h3>
        <ol>
          <li value="1">	Actuar sin buscar intereses particulares, ni beneficios personales, para familiares o amistades. Esto implicará no involucrarse en situaciones o actividades que signifiquen conflicto de intereses, personales o familiares, en el desempeño de sus labores.
          </li>
          <li value="2">	Encausar en el desempeño de la responsabilidad conferida con   imparcialidad, respetando el derecho de todas las personas y rechazando cualquier procedimiento que privilegie ventajas personales o de grupo.
          </li>
          <li value="3">	No utilizar información privilegiada en beneficio propio, de terceros o para    fines distintos a los que son inherentes a su responsabilidad.
          </li>
          <li value="4">	No aceptar, ofrecer, ni otorgar directa o indirectamente, dinero, dádivas, favores o ventajas a cambio de la realización u omisión de cualquier acto en el ejercicio de su responsabilidad.</li>
          <li value="5">	Privilegiar el diálogo y la negociación en la resolución de conflictos.</li>
          <li value="6">	VHC promoverá la competencia justa, evitando caer en prácticas diferentes a las estipuladas en los términos contratados. </li>
        </ol>
        <h3>IV.	Responsabilidad con la Información, su comunicación y confidencialidad</h3>
        <ol>
          <li>	Asegurar que la información que difunda VHC, sea veraz, oportuna, adecuada, transparente, de provecho y suficiente para cumplir con la exigencia del derecho a la información.
          </li>
          <li> 	Respetar la dignidad de las personas, siendo justo, veraz y preciso en sus apreciaciones. Reconocer el pleno derecho y la legitimidad de las diversas opiniones.
          </li>
          <li> 	Propiciar que en los procesos de decisión se consideren los aspectos éticos del caso.
          </li>
          <li> 	Ejercer la autoridad con responsabilidad y aplicar en ello todo el tiempo y esfuerzo necesario y suficiente, resguardando siempre la dignidad, la honra, el prestigio y el buen nombre de VHC.
          </li>
          <li> 	Mantener registros completos y precisos de los proyectos en los cuales se intervenga, cumpliendo así con las políticas internas de la firma, con requisitos de carácter legal y con las normas profesionales aplicables.
          </li>
          <li> 	Cumplir con los procedimientos establecidos para el tratamiento de datos personales (obtención, uso, divulgación, almacenamiento, cancelación, supresión), con la finalidad de garantizar la privacidad y el derecho de las personas para decidir sobre el uso de la información que les pertenece.
          </li>
          <li> 	Guardar escrupulosamente los datos personales, secretos técnicos, comerciales y de prestación de servicios a cuya elaboración concurran directa o indirectamente, o de los cuales tengan conocimiento por razón del trabajo que desempeñen, así como de los asuntos administrativos reservados, cuya divulgación pueda causar perjuicios a VHC, así como a sus clientes, alianzas, proveedores y demás colaboradores.
          </li>
          <li> 	Utilizar la información que se obtenga gracias al trabajo desarrollado en la firma, con fines estrictamente profesionales. No podrá utilizarse para beneficio o cuestiones personales. Para lo cual, existe con los clientes de la firma, acuerdo de confidencialidad que protege su información revelada y generada en el transcurso de nuestra labor profesional.
          </li>
          <li> 	Está prohibido compartir información confidencial de clientes y ex clientes, con cualquier persona ajena a la firma, incluso con empleados de éstos que no deban tener acceso a dicha información, o con integrantes de la firma que no estén interviniendo en el proyecto, salvo por razones legales o profesionales, o se tenga autorización por escrito por parte del cliente o ex cliente.
          </li>
          <li> Asegurarse que en todos los proyectos y con los clientes asignados, se celebre y se firme el convenio de confidencialidad elaborado por VHC.
          </li>
        </ol>


