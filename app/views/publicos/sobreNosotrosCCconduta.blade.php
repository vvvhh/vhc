          <li> 	Trabajar con clientes, personas y/o entidades asignados, en donde no existirá relación interna, externa o de cualquier tipo, más importante que la ética, la integridad y la reputación de VHC; como profesionales, el principal compromiso será el de no defraudar la confianza que se deposita en nosotros.
          </li>
          <li> 	Los miembros de VHC, o aquellos que trabajen en su representación y quienes interactúan con la firma, tendrán derecho a un trato digno y respetuoso en los lugares de trabajo donde desempeñan sus actividades.
