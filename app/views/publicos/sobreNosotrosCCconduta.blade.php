                <h3>Introducción </h3>
                <p>
                  Este código es una afirmación formal de los valores que definen los estándares del comportamiento específico mínimo que esperamos adopten todos nuestros colaboradores al formar parte de VHC; aquí encontrarán de forma explícita, las normas del comportamiento que deberán demostrar al ser un profesional y miembro de nuestra firma. Sostenemos que nuestro actuar tiene una dimensión ética, que contempla la correcta selección de los medios para realizar objetivos dignos y valiosos, no es justificable que los fines sean absolutos y mediaticen al hombre, tampoco que el uso de medios dañen la dignidad de las personas.
                </p>
                <p>
                  Al final, puede existir un sinfín de circunstancias en las cuales podemos encontrarnos inmersos, sin embargo, lo más importante será hacer valer la formación y responsabilidad ética de cada uno de los que integramos VHC, así como nuestro juicio profesional.
                </p>


                <h3>Objetivo </h3>
                <p>
                  Ofrecer una guía de conducta profesional a los miembros de Vázquez Hernández Contadores, S.C. (VHC), que les permita escoger la opción éticamente más apropiada ante cualquier situación, así como evitar en lo posible que puedan determinar, que si una situación no está específicamente prohibida entonces está permitida.
                </p>
                <p>
                  A continuación, plasmamos nuestra filosofía organizacional, misma que es la base sobre la que se sustenta nuestro código de conducta.
                </p>


            <h3>Misión</h3>
            <p>
              Contribuir con nuestros servicios en contaduría a satisfacer las necesidades de la comunidad empresarial aportando valor en nuestro actuar, basándonos en el capital intelectual de nuestra firma, bajo un marco de confianza, ética profesional, mejora continua y calidad.
            </p>


            <h3>Visión</h3>
            <p>
