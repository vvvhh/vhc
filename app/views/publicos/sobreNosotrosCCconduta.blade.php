        <h3>VII.	Procedimientos para consultas y Consecuencias</h3>
        <h4><u>Procedimiento de consultas</u></h4>

        <p>
          La finalidad de nuestro Código de Conducta es establecer sólo una guía de conducta que permita escoger la opción éticamente más apropiada ante cualquier situación, por lo que es imposible contemplar todas las circunstancias posibles en las cuales es preciso actuar. De esta forma, sabemos que existirán momentos en los cuales habrá dudas respecto a la formar de reaccionar ante cierta situación, es por ello que VHC exhorta a todos sus miembros a comunicar cualquier duda, inquietud, preocupación, sugerencia, y reporte de sospecha de violación al presente código, mediante los siguientes canales:

        </p>
        <ol type="a">
          <li>Jefe inmediato</li>
          <li>	Gestor de calidad</li>
          <li>	Dirección General</li>
          <li>	Socios de la firma</li>

        </ol>

        <h4><u>Consecuencias</u></h4>
        <p>
          El cumplimiento del Código de Conducta beneficia a todos los que conformamos VHC no sólo en el ámbito profesional, sino también en el personal, éste se ve reflejado en nuestro desempeño y en la imagen que ofrecemos al exterior. Debido a lo anterior, es que en caso de que algún miembro actúe en desacato a este código, habrá una consecuencia, misma que podrá consistir desde una llamada de atención y hasta la terminación de la relación laboral, independientemente de cualquier acción, obligación o sanción legal que corresponda.
        </p>
        <p>
          En caso de que algún empleado de VHC viole alguna política de confidencialidad, ética y conducta relacionada con sus actividades laborales, será sujeto de una acción disciplinaria, misma que podría ser incluso la conclusión inmediata de su relación laboral con la firma, también serán sujetos de estas acciones los colaboradores que:
        </p>
        <ol type="a">
          <li>	No informen en el momento en el que se presente una situación que ponga en peligro la seguridad y el patrimonio de la firma,
          </li>
          <li>		No entreguen información relacionada a una posible falta a la ley o de este código de conducta.
           </li>
          <li>		Tomen alguna acción en contra de cualquier persona o colaborador de la firma que los haya reportado por una sospecha de violación al presente código, a las políticas internas y procedimientos de la firma, o incluso a alguna disposición legal.
          </li>
        </ol>

        <p>
          Por todo lo anteriormente señalado, los integrantes de esta firma que violen este código, se harán acreedores a las sanciones que de manera discrecional les imponga la dirección.
        </p>

        <p>
          Para la imposición de las sanciones se tomará en cuenta la gravedad, y la reincidencia de la violación cometida, evaluando dicha gravedad de acuerdo con la trascendencia que la falta tenga para el prestigio y estabilidad de la firma así como para la integridad de sus miembros, según la gravedad de la falta la sanción podrá consistir en:
        </p>

        <ol type="a">
          <li>	Amonestación (llamada de atención y levantamiento de actas administrativas)
          </li>
          <li>	Suspensión temporal de sus actividades y responsabilidades
