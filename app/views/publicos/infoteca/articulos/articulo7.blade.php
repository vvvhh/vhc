        <h4>¿Qué son los derechos ARCO?</h4>
        <p class="text-justify">
          Son los derechos que pueden ejercer los individuos para proteger sus datos personales. La palabra ARCO se refiere a <b>A</b>cceder, <b>R</b>ectificar, <b>C</b>ancelar y <b>O</b>ponerse al tratamiento de sus datos personales.
        </p>

        <p class="text-justify">
          El derecho de <strong>Acceso</strong>, es la puerta de entrada para que conozcas cuáles de tus datos personales están en poder de cierta entidad o persona, y de esta forma estés en posibilidades de ejercer los demás derechos.
        </p>

        <p class="text-justify">
          El derecho de <strong>Rectificación</strong>, está enfocado a controlar la calidad de la información, esto es si existen errores en tus datos personales o es necesario actualizarlos, entonces es momento de ejercer esta facultad.
        </p>
        <p class="text-justify">
          El derecho de <strong>Cancelación</strong> implica la solicitud de eliminación de tus datos personales porque éstos están siendo tratados indebidamente.
        </p>
        <p class="text-justify">
          El derecho de <strong>Oposición</strong> se puede ejercer cuando no queremos que nuestros datos personales sean utilizados para ciertos fines, es decir, daremos autorización para que nuestra información sea utilizada para ciertas actividades y para otras no.
        </p>

        <ul>
          <li>
            Cuando deseamos conocer los datos personales que tiene registrados cierta institución;
          </li>
          <li>
            Cuando es preciso actualizarlos debido a que ciertos datos personales son incorrectos, por ejemplo: el domicilio que tiene registrado el banco para la entrega de nuestros estados de cuenta, o el número telefónico;
          </li>
          <li>
            Cuando creemos que nuestros datos personales han sido vulnerados y por tal motivo deseamos que los supriman de ciertas bases de datos, por ejemplo, cuando recibes llamadas de una compañía de servicio de telefonía celular diferente a la que te presta el servicio, cuando recibes llamadas del banco para ofrecerte nuevos servicios, etcétera, y la característica principal, es que son empresas de las que nunca en la vida habías escuchado y mucho menos recuerdas haberles proporcionado tus datos, inclusive cuando contrataste algún tipo de servicio por única ocasión y que posteriormente continúan constantemente llamando para ofrecerte nuevos productos o servicios;
          </li>
          <li>
            Cuando nuestros datos personales están siendo utilizados para otros fines, diferentes a lo que inicialmente autorizaste. Por ejemplo, cuando contratamos el servicio de hospedaje, nuestros datos son indispensable para que a la empresa le sea posible prestarnos el servicio, pero sólo para eso, no obstante la empresa hotelera utiliza tus datos para llamar constantemente y ofrecerte descuentos y promociones, cuando tú no los has solicitado.

          </li>
        </ul>

        <h4>¿Cómo puedes ejercitar tus derechos ARCO?
        </h4>
        <p class="text-justify">
          Muy fácil, sólo tienes que dar lectura al Aviso de Privacidad de la empresa o persona que está utilizando tus datos personales y verificar cuál es el procedimiento y los requisitos para ejercer tus derechos ARCO. Lo anterior puede ser mediante el envío, vía correo electrónico o certificado, de una solicitud al Encargado del Departamento de Datos Personales de la empresa en cuestión o acudiendo personalmente a ejercer tus derechos. Dicha solicitud deberá contener por lo menos los siguientes datos: tu nombre, domicilio o <i>email</i> para comunicarte la respuesta, copia de alguna identificación, y una descripción clara y precisa de los datos personales respecto de los cuales deseas ejercer alguno de los derechos ARCO.
        </p>
        <br>

        <p  class="text-justify">
            El responsable de la entidad del manejo de tus datos personales deberá dar respuesta a tu petición a más tardar en 20 días hábiles, siempre y cuando hayas enviado en su totalidad los datos requeridos en la solicitud.
        </p>

