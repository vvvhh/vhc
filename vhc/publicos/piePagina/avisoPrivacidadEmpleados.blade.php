            <h4 class="grisObscuro"><strong>1. IDENTIDAD Y DOMICILIO DEL RESPONSABLE DE SUS DATOS PERSONALES.</strong></h4>
            <strong>VÁZQUEZ HERNÁNDEZ CONTADORES, S.C.</strong>, es la persona moral que será responsable del tratamiento de sus datos personales, a quien en lo sucesivo se le denominará: VHC, con domicilio en Huatabampo número 72, Colonia Roma Sur, Delegación Cuauhtémoc, Código Postal 06760, México, Distrito Federal, Teléfono (55) 52647313, domicilio que señala para recibir todo tipo de notificaciones y para los efectos legales conducentes.
            <br><br><br>
            <h4 class="grisObscuro"><strong>2. DATOS PERSONALES Y DATOS PERSONALES SENSIBLES.</strong></h4>
            Para efectos de la Ley, se entiende por DATOS PERSONALES cualquier información  concerniente a una persona física identificada o identificable, a la cual se denominará en adelante el TITULAR, expresados en forma numérica, alfabética, gráfica, fotográfica, en audio o video.
            <br><br><br>
            <h4 class="grisObscuro"><strong>3. FINALIDAD DEL TRATAMIENTO DE SUS DATOS PERSONALES.</strong></h4>
            VHC podrá recabar del titular los datos personales que sean necesarios para la adecuada prestación de los servicios que presta a su favor, tales como:
            <br><br>
            a) Prestación de Servicios Profesionales que el titular requiera, de conformidad con la propuesta que VHC le presente de los mismos en los ramos de contaduría comprendiendo: contabilidad,  auditoría; la asesoría financiera, fiscal, soluciones de ingresos y costos, administración del cambio y gestión de proyectos y riesgos operacionales, mejora de procesos, manejo de transacciones y manejo de crisis, entre otros; asesoría legal, en recursos humanos, así como servicios de administración, de gestión administrativa y contable y todos aquellos relacionados con los anteriores para asesorar a personas físicas y morales nacionales y extranjeras.
            <br><br>
            b) Cumplimiento de las obligaciones contraídas con el titular de acuerdo a los servicios profesionales que se le presten, así como informarle cualquier situación o cambio sobre dichos servicios.
            <br><br>
            c) Evaluación de la calidad de los servicios profesionales que presta VHC y aplicación de encuestas de satisfacción de clientes.
            <br><br>
            d) Mantener contacto con el titular, con objeto de invitarlo a conferencias, cursos o eventos, en el que se tratan temas de actualidad que se relacionen con los servicios profesionales que VHC le presta, así como brindar servicios mercadotécnicos, publicitarios o de prospección comercial.
            <br><br>
            e) Dar respuesta a las personas que siendo usuarios del sitio web y sin ser clientes de VHC, planten sus dudas sobre un problema específico y se les ofrezca información detallada sobre los servicios que brinda VHC y que pueden ser implementados para la solución de sus problemas.
            <br><br>
            En consecuencia, el tratamiento de los datos personales será realizado mediante la obtención, uso, divulgación o almacenamiento de datos personales, por cualquier medio. Para fines del presente aviso el uso abarca cualquier acción de acceso, manejo, aprovechamiento, transferencia o disposición de datos personales para los demás fines y usos establecidos en la Ley y reglamentos aplicables.
            <br><br><br>
            <h4 class="grisObscuro"><strong>4. DATOS PERSONALES QUE SERÁN TRATADOS POR VHC.</strong></h4>
            Para las finalidades antes mencionadas VHC podrá obtener del titular los datos personales que requiera y que de forma enunciativa y no limitativa, se mencionan a continuación: a) Nombre completo; b) Domicilio laboral y particular; c) Datos de información personal (tales como  edad, genero, estado civil, nacionalidad); d) Datos de información gubernamental (Registro Federal de Contribuyentes (RFC), Clave Única de Registro de Población (CURP), Número de seguridad social o Registro Patronal ante el Instituto Mexicano del Seguro Social; e) Correo electrónico; f) Teléfono laboral y particular; g) Teléfono celular; h) Información financiera y patrimonial; i) Datos de terceros relacionados con el titular; k) Los datos personales relacionados con la prestación de los servicios de VHC.
            <br><br><br>
            <h4 class="grisObscuro"><strong>5. PLAZOS PARA EL TRATAMIENTO DE LOS DATOS PERSONALES.</strong></h4>
            La temporalidad del manejo y tratamiento de los datos personales y/o sensibles, dependerá directamente de la relación jurídica que se tenga celebrada con VHC, así como de las obligaciones exigidas por la legislación vigente y las autoridades competentes. En todos los casos la información será conservada por el plazo necesario para el cumplimiento de las finalidades que justificaron su tratamiento y para dar cumplimiento a las responsabilidades nacidas como consecuencias del propio tratamiento.
            <br><br><br>
            <h4 class="grisObscuro"><strong>6. PROTECCIÓN DE DATOS PERSONALES.</strong></h4>
            VHC cumple con los principios rectores de protección de datos personales de licitud, consentimiento, información, finalidad, proporcionalidad, lealtad, calidad y responsabilidad establecidos por la Ley. VHC adoptará los medios y mecanismos necesarios y suficientes para procurar que el tratamiento de los datos personales sea en apego a la legislación aplicable, privilegiando en todo momento la protección de sus intereses y privacidad mediante su debido resguardo y protección a efecto de que se impida su pérdida, mal uso, alteración, acceso no autorizado y/o robo. En consecuencia, sólo tendrán acceso a la información aquellas personas autorizadas por VHC quienes han asumido el compromiso de mantener la información bajo un estricto orden de confidencialidad y seguridad.
            <br><br><br>
